    {
        $brands = \App\Brand::pluck('name', 'id')->all();
        $manufactures = \App\Manufacture::pluck('name', 'id')->all();
        $descriptions = \App\Description::pluck('name', 'id')->all();
        return view('products.create',  compact('brands', 'manufactures', 'descriptions'));
    }

